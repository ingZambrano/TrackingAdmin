package mx.com.avior.dto;

public class DTOTelefonica extends DTOBase{
	
	private int fiId;
	private String fcNombre;
	private String fcDescripcion;
	private int fiIdStatus;
	public int getFiId() {
		return fiId;
	}
	public void setFiId(int fiId) {
		this.fiId = fiId;
	}
	public String getFcNombre() {
		return fcNombre;
	}
	public void setFcNombre(String fcNombre) {
		this.fcNombre = fcNombre;
	}
	public String getFcDescripcion() {
		return fcDescripcion;
	}
	public void setFcDescripcion(String fcDescripcion) {
		this.fcDescripcion = fcDescripcion;
	}
	public int getFiIdStatus() {
		return fiIdStatus;
	}
	public void setFiIdStatus(int fiIdStatus) {
		this.fiIdStatus = fiIdStatus;
	}
	
	

}
